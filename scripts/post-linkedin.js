const Anthropic = require('@anthropic-ai/sdk');
const fs = require('fs');
const path = require('path');

// ── Config ──
const POSTS_FILE = path.join(__dirname, '..', 'data', 'posts.json');
const LINKEDIN_ACCESS_TOKEN = process.env.LINKEDIN_ACCESS_TOKEN;
const LINKEDIN_PERSON_URN = process.env.LINKEDIN_PERSON_URN;

// ── Main ──
async function main() {
  if (!LINKEDIN_ACCESS_TOKEN || !LINKEDIN_PERSON_URN) {
    console.error('❌ Faltan LINKEDIN_ACCESS_TOKEN o LINKEDIN_PERSON_URN en env vars');
    process.exit(1);
  }

  // 1. Leer noticias de hoy
  const postsData = JSON.parse(fs.readFileSync(POSTS_FILE, 'utf8'));
  const today = new Date().toISOString().split('T')[0];
  const todayNews = postsData.posts.filter(p => p.autoGenerated && p.date === today);

  if (todayNews.length === 0) {
    console.log('⚠️ No hay noticias nuevas hoy. No se publica en LinkedIn.');
    process.exit(0);
  }

  console.log(`📰 ${todayNews.length} noticias de hoy encontradas`);

  // 2. Generar resumen con Claude
  const postText = await generateSummary(todayNews);
  console.log('📝 Post generado:\n');
  console.log(postText);
  console.log('\n---');

  // 3. Publicar en LinkedIn
  await publishToLinkedIn(postText);
}

// ── Generar resumen con Claude ──
async function generateSummary(news) {
  const client = new Anthropic();

  const newsData = news.map(n => ({
    title: n.title,
    category: n.category || n.folder,
    description: n.description,
    tags: n.tags,
    sourceName: n.sourceName,
  }));

  const response = await client.messages.create({
    model: 'claude-sonnet-4-20250514',
    max_tokens: 1500,
    messages: [{
      role: 'user',
      content: `Genera un post para LinkedIn en español resumiendo estas noticias de IA publicadas hoy.

Noticias del día:
${JSON.stringify(newsData, null, 2)}

FORMATO OBLIGATORIO (respeta exactamente este estilo, tono profesional pero cercano, con emojis):

---
Hoy en txemagonzalez.com se han publicado varias novedades que, vistas en conjunto, dibujan muy bien hacia dónde va el mundo LLM + Agents + RAG.

🧠 Investigación
[Título o tema]
[Comentario de 1-2 líneas con insight de developer, opinión breve o dato relevante]

🤖 Agentes
• [Título]
[Comentario breve]

🛠️ Herramientas
• [Título]
[Comentario breve]

🧩 LLM
• [Título]
[Comentario breve, puede incluir toque de humor con emoji]

📌 Conclusión rápida:
[3-4 frases cortas tipo manifiesto, una por línea, específicas a las noticias de hoy]

Todas estas noticias están agregadas y se actualizan a diario en 👉 txemagonzalez.com

#AI #Agents #LLM #RAG #GenAI #SoftwareArchitecture #IA
---

REGLAS:
- Agrupa noticias por categoría. Usa estos iconos: 🧩 LLM, 🤖 Agentes, 🛠️ Herramientas, 📦 Repos GitHub, ✅ Buenas Prácticas, 🧠 Investigación
- Si hay varias noticias de la misma categoría, ponlas como bullets (•) bajo el mismo header
- Si solo hay una categoría, no pasa nada, pon las que haya
- La conclusión debe ser ESPECÍFICA a las noticias de hoy, NO genérica
- Máximo 1500 caracteres (límite blando de LinkedIn para visibilidad sin "ver más")
- Devuelve SOLO el texto del post, sin comillas, sin backticks, sin markdown`
    }]
  });

  return response.content[0].text.trim();
}

// ── Publicar en LinkedIn ──
async function publishToLinkedIn(text) {
  console.log('📤 Publicando en LinkedIn...');

  const body = {
    author: `urn:li:person:${LINKEDIN_PERSON_URN}`,
    lifecycleState: 'PUBLISHED',
    specificContent: {
      'com.linkedin.ugc.ShareContent': {
        shareCommentary: {
          text: text,
        },
        shareMediaCategory: 'ARTICLE',
        media: [
          {
            status: 'READY',
            originalUrl: 'https://txemagonzalez.com/',
            title: {
              text: '🤖 Noticias IA del día - Code 4 All',
            },
            description: {
              text: 'Noticias diarias de IA, LLMs, agentes y herramientas para developers',
            },
          },
        ],
      },
    },
    visibility: {
      'com.linkedin.ugc.MemberNetworkVisibility': 'PUBLIC',
    },
  };

  const res = await fetch('https://api.linkedin.com/v2/ugcPosts', {
    method: 'POST',
    headers: {
      'Authorization': `Bearer ${LINKEDIN_ACCESS_TOKEN}`,
      'Content-Type': 'application/json',
      'X-Restli-Protocol-Version': '2.0.0',
    },
    body: JSON.stringify(body),
  });

  if (res.ok || res.status === 201) {
    const data = await res.json();
    console.log('✅ Publicado en LinkedIn. ID:', data.id);
    return;
  }

  const errorText = await res.text();
  console.error(`❌ Error ${res.status} publicando en LinkedIn:`, errorText);

  // Si es 401 (token expirado), intentar refresh
  if (res.status === 401) {
    console.log('🔄 Token expirado. Intentando refresh...');
    const newToken = await refreshToken();
    if (newToken) {
      // Reintentar con token nuevo
      const res2 = await fetch('https://api.linkedin.com/v2/ugcPosts', {
        method: 'POST',
        headers: {
          'Authorization': `Bearer ${newToken}`,
          'Content-Type': 'application/json',
          'X-Restli-Protocol-Version': '2.0.0',
        },
        body: JSON.stringify(body),
      });

      if (res2.ok || res2.status === 201) {
        const data2 = await res2.json();
        console.log('✅ Publicado con token renovado. ID:', data2.id);
        return;
      }

      console.error('❌ Falló con token renovado:', await res2.text());
    }
    process.exit(1);
  }

  process.exit(1);
}

// ── Refresh token ──
async function refreshToken() {
  const clientId = process.env.LINKEDIN_CLIENT_ID;
  const clientSecret = process.env.LINKEDIN_CLIENT_SECRET;
  const refreshTk = process.env.LINKEDIN_REFRESH_TOKEN;

  if (!clientId || !clientSecret || !refreshTk) {
    console.error('❌ Faltan credenciales para refresh (CLIENT_ID, CLIENT_SECRET o REFRESH_TOKEN)');
    return null;
  }

  const params = new URLSearchParams({
    grant_type: 'refresh_token',
    refresh_token: refreshTk,
    client_id: clientId,
    client_secret: clientSecret,
  });

  const res = await fetch('https://www.linkedin.com/oauth/v2/accessToken', {
    method: 'POST',
    headers: { 'Content-Type': 'application/x-www-form-urlencoded' },
    body: params.toString(),
  });

  if (res.ok) {
    const data = await res.json();
    console.log('✅ Token renovado correctamente');
    console.log('⚠️ IMPORTANTE: Actualiza el secret LINKEDIN_ACCESS_TOKEN en GitHub con este nuevo token.');
    return data.access_token;
  }

  console.error('❌ Error renovando token:', await res.text());
  return null;
}

// ── Run ──
main().catch(e => {
  console.error('❌ Error fatal:', e);
  process.exit(1);
});
